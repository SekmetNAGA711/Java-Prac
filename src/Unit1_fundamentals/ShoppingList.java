package Unit1_fundamentals;

public class ShoppingList {



    public static void main(String[] args) {
        System.out.println("SHOPPING LIST");
        System.out.println("- Milk");
        System.out.println("\t > Semi-skimmed");
        System.out.println("\t > Medium");
        System.out.println("- Eggs");
        System.out.println("\t > Free-range");
        System.out.println("- Bread \n");

        singleLine();

    }

    public static void singleLine () {


        System.out.println("SHOPPING LIST \n - Milk\" \n \t > Semi-skimmed \n \t > Medium \n - Eggs \n \t > Free-range \n - Bread ");
    }



}
